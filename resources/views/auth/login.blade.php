<script>
    getContra()

    function getContra(){
        setTimeout(() => {
            const check = getCookie("check");
            if(check==="true"){
                document.getElementById('remember').checked=true
                const contra = getCookie("contra");
                document.getElementById('password').value=contra;
                const usu = getCookie("usu");
                document.getElementById('email').value=usu;
            }else{
                document.getElementById('remember').checked=false
            }
        }, 500);
    }

    function guardarContra(){
        const check= document.getElementById('remember').checked;
        document.cookie="check="+check+" ; expires=31 Dec 2023 23:59:59 GMT";
        if(check===true){
            const contra= document.getElementById('password').value;
            document.cookie="contra="+contra+" ; expires=31 Dec 2023 23:59:59 GMT";
            const usu= document.getElementById('email').value;
            document.cookie="usu="+usu+" ; expires=31 Dec 2023 23:59:59 GMT";
        }else{
            document.cookie="contra= ; expires=31 Dec 2023 23:59:59 GMT";
            document.cookie="usu= ; expires=31 Dec 2023 23:59:59 GMT";
        }  
    }

    function getCookie(cname) {
        let name = cname + "=";
        let decodedCookie = decodeURIComponent(document.cookie);
        let ca = decodedCookie.split(';');
        for(let i = 0; i <ca.length; i++) {
            let c = ca[i];
            while (c.charAt(0) == ' ') {
            c = c.substring(1);
            }
            if (c.indexOf(name) == 0) {
            return c.substring(name.length, c.length);
            }
        }
        return "";
    }
    
</script>
